package hw7_2;

public class User {
	private String id;
	private String password;
	private String name;
	private int age;
	
	public User() {
		super();
	}

	public User(String id, String password, String name, int age) {
		super();
		this.id = id;
		this.password = password;
		this.name = name;
		this.age = age;
	}
	
	
	
}
